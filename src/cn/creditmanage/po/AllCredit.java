public class AllCredit {

    private String username;

    private int bsscore;

    private int lascore;

    private int rpscore;

    private int twscore;

    private int rescore;

    private int allscore;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getBsscore() {
        return bsscore;
    }

    public void setBsscore(int bsscore) {
        this.bsscore = bsscore;
    }

    public int getLascore() {
        return lascore;
    }

    public void setLascore(int lascore) {
        this.lascore = lascore;
    }

    public int getRpscore() {
        return rpscore;
    }

    public void setRpscore(int rpscore) {
        this.rpscore = rpscore;
    }

    public int getTwscore() {
        return twscore;
    }

    public void setTwscore(int twscore) {
        this.twscore = twscore;
    }

    public int getRescore() {
        return rescore;
    }

    public void setRescore(int rescore) {
        this.rescore = rescore;
    }

    public int getAllscore() {
        return allscore;
    }

    public void setAllscore(int allscore) {
        this.allscore = allscore;
    }
}
